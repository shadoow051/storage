//≡≡≡≡≡≡≡≡≡≡≡≡≡≡≡≡≡≡≡≡≡≡≡≡≡≡≡≡≡≡≡≡≡≡≡≡≡≡≡≡≡≡≡≡≡≡≡≡≡≡≡≡≡≡≡≡≡≡≡≡≡≡≡≡≡≡≡≡≡

class StorageManager {
  constructor() {
    this.name = null;
    this.type = StorageType.Json;
    this.functions = JsonStorage;
  }

  /**
   * @param {string} filePath
   */
  async setStorage(filePath) {
    try {
      const module = await import(pathToFileURL(filePath).href);
      this.functions = module.default || module;
      this.type = StorageType.Custom;
    } catch (error) {
      console.error(error);
    }
  }

  /**
   * @param {string} storageType
   */
  setDefaultStorage(storageType) {
    try {
      switch (storageType) {
        case StorageType.Json:
          {
            this.functions = JsonStorage;
            this.type = StorageType.Json;
          }
          break;
        default:
          {
            this.functions = JsonStorage;
            this.type = StorageType.Json;
          }
          break;
      }
    } catch (error) {
      console.error(error);
    }
  }
}

