let Storage = JsonStorage;

/**
 * @param {string} filePath
 */
async function SetStorage(filePath) {
  try {
    const module = await import(`file://${filePath}`);
    Storage = module.default || module;
    return true;
  } catch (error) {
    Storage = JsonStorage;
    console.error(error);
    return false;
  }
}

